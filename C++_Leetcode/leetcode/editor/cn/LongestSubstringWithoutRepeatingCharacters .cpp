#include<bits/stdc++.h>
#include<unordered_map>

using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    int lengthOfLongestSubstring(string s) {
        unordered_map<char, int> windows;
        int right = 0;
        int left = 0;
        int lens = 0;
        while (right < s.size()) {
            char c = s[right];
            right++;
            windows[c]++;
            while (windows[c] > 1) {
                char temp = s[left];
                left++;
                windows[temp]--;
            }
            lens = max(lens, right - left);

        }
        return  lens;

    }
};
