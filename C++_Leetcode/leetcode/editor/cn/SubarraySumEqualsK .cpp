class Solution {
public:
    int subarraySum(vector<int>& nums, int k) {
    int res = 0;
    int lens = nums.size();
        for (int i = 0; i < lens; ++i) {
            int sum = 0;
            for (int j = i ; j < lens; ++j) {
                sum+=nums[j];
                if (sum==k)res++;
            }
        }
        return res;
    }
};
